            <section class="Comofaz">
                <h1>Como faz</h1>
                <ol>
                    <li>Em uma panela média, coloque o Azeite TERRANO® e leve ao fogo alto para aquecer. Junte o alho e a cebola, e refogue por 1 minuto, ou até o alho começar a dourar. Acrescente o frango, o Tempero SAZÓN® e o sal, e frite por 6 minutos, mexendo de vez em quando, ou até dourar o frango por todos os lados.</li>
                    <li>Adicione o ketchup, a mostarda, a polpa de tomate e o cogumelo, e misture. Junte o creme de leite, mexa e cozinhe por 2 minutos, ou até começar a levantar fervura.</li>
                    <li>Retire do fogo e sirva em seguida.</li>
                </ol>
            </section>
        
