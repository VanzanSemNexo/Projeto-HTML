                <h1>Nome da comida</h1>
                <p>O strogonoff de frango está na lista dos pratos mais amados e aclamados entre os brasileiros. Com um tempero único e uma suculência sem igual, essa receita é uma ótima opção tanto para o dia a dia quanto para ocasiões especiais, como um almoço de domingo em família ou um jantar divertido com os amigos.

                    Mas, por que usar o frango no lugar da carne ou do camarão? O frango chama atenção pela facilidade no preparo e o valor mais em conta.</p>
